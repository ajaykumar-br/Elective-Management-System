  <p>


  	<div>
  		Module – 1 

		Introduction Fundamental Steps in Digital Image Processing, Components of an
Image Processing System, Sampling and Quantization, Representing Digital
Images (Data structure), Some Basic Relationships Between Pixels- Neighbors
and Connectivity of pixels in image, Applications of Image Processing: Medical
imaging, Robot vision, Character recognition, Remote Sensing.
	</div>


	<div>
  		Module – 2
  		Image Enhancement In The Spatial Domain: Some Basic Gray Level
Transformations, Histogram Processing, Enhancement Using Arithmetic/Logic
Operations, Basics of Spatial Filtering, Smoothing Spatial Filters, Sharpening
Spatial Filters, Combining Spatial Enhancement Methods.
	</div>


	<div>
		Module-3
  		Image Enhancement In Frequency Domain:
Introduction, Fourier Transform, Discrete Fourier Transform (DFT), properties
of DFT , Discrete Cosine Transform (DCT), Image filtering in frequency domain
	</div>

	<div>
  		Module-4
  		Image Segmentation: Introduction, Detection of isolated points, line detection,
Edge detection, Edge linking, Region based segmentation- Region growing, split
and merge technique, local processing, regional processing, Hough transform,
Segmentation using Threshold.
	</div>

	<div>
  		Module – 5
Image Compression: Introduction, coding Redundancy , Inter-pixel redundancy,
image compression model, Lossy and Lossless compression, Huffman Coding,
Arithmetic Coding, LZW coding, Transform Coding, Sub-image size selection,
blocking, DCT implementation using FFT, Run length coding.
	</div>

