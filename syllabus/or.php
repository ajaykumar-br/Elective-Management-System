  	<div>
  		Module – 1 

		Introduction, Linear Programming: Introduction: The origin, nature and
impact of OR; Defining the problem and gathering data; Formulating a
mathematical model; Deriving solutions from the model; Testing the model;
Preparing to apply the model; Implementation .
Introduction to Linear Programming Problem (LPP): Prototype example,
Assumptions of LPP, Formulation of LPP and Graphical method various
examples.

	</div>


	<div>
  		Module – 2
  		Simplex Method – 1: The essence of the simplex method; Setting up the simplex
method; Types of variables, Algebra of the simplex method; the simplex method
in tabular form; Tie breaking in the simplex method, Big M method, Two phase
method.
	</div>


	<div>
		Module-3
  		Duality Theory - The essence of duality theory, Primal
dual relationship, conversion of primal to dual problem and vice versa. The dual
simplex method.

	</div>

	<div>
  		Module-4
  		Transportation and Assignment Problems: The transportation problem, Initial
Basic Feasible Solution (IBFS) by North West Corner Rule method, Matrix
Minima Method, Vogel’s Approximation Method. Optimal solution by Modified
Distribution Method (MODI). The Assignment problem; A Hungarian algorithm
for the assignment problem. Minimization and Maximization varieties in
transportation and assignment problems.
	</div>

	<div>
  		Module – 5
Game Theory: Game Theory: The formulation of two persons, zero sum games;
saddle point, maximin and minimax principle, Solving simple games- a prototype
example; Games with mixed strategies; Graphical solution procedure.
Metaheuristics: The nature of Metaheuristics, Tabu Search, Simulated
Annealing, Genetic Algorithms.
	</div>

